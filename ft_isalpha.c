#include "libft.h"

// c -> valeur ASCII
// [65;90] -> Uppercase 
// [97;122] -> Lowercase

int    ft_isalpha(int c)
{
    if ((c >= 65 && c <= 90) || (c >= 97 && c <= 122))
    {
        return (c);
    }
    else
    {
        return (0);
    }
}
