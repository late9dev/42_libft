#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*tmp_src;
	size_t	i;

	tmp_src = (char *) s;
	i = 0;
	while (n > 0)
	{
		tmp_src[i] = 0;
		i++;
		n--;
	}
}
